package org.xwiki.gwt.wysiwyg.client.syntax;

import org.xwiki.gwt.user.client.ui.rta.RichTextArea;

/**
 * A syntax validator ensures that the XHTML code generated by the editor can be converted without loss of information
 * to the underlying syntax. it does this by restricting the user actions (UI features) when they can generate XHTML
 * constructs that don't have a correspondent in the underlying syntax.
 * 
 * @version $Id$
 */
public interface SyntaxValidator
{
    /**
     * Adds a new validation rule to the list of rules this syntax validator obeys.
     * 
     * @param rule The validation rule to be added.
     */
    void addValidationRule(ValidationRule rule);

    /**
     * Verifies if a specific feature of the editor should be enabled, considering the current list of validation rules
     * and the current state of the given text area.
     * 
     * @param feature The feature to be tested.
     * @param textArea The text area whose current state should be considered.
     * @return true if there's no rule that disables the specified feature.
     */
    boolean isValid(String feature, RichTextArea textArea);

    /**
     * Removes the specified rule from the list of validation rules this syntax validator obeys.
     * 
     * @param rule The validation rule to be removed.
     */
    void removeValidationRule(ValidationRule rule);

    /**
     * @return A string identifier for the syntax characterized by the underlying validation rules.
     */
    String getSyntax();
}
